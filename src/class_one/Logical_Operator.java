package class_one;

/**
 * 逻辑运算符
 */
public class Logical_Operator {
    public static void main(String[] args) {
        boolean a = true;
        boolean b = false;
        System.out.println(a&b);
        System.out.println(a|b);
        System.out.println(a^b);// 两遍相同为 false 相同为true
        System.out.println(a!=b);
        System.out.println(a&&b);//短路与
        System.out.println(a||b);//短路或




        //面试题
    }
}